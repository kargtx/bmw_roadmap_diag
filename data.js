// Функция для получения времени последней синхронизации
function getLastSyncTime() {
    const lastSync = localStorage.getItem('lastSyncTime');
    if (lastSync) {
        return new Date(parseInt(lastSync)).toLocaleString();
    }
    return 'никогда';
}